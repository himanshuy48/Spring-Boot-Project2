package com.digi.uniprr.VO;

import java.util.List;

public class ReviewerFilterVO {
	
	private List<Integer> journalId;
	
	private List<Integer> expertiseId;
	
	private List<Integer> specializationId;
	
	private List<String> institutionId;
	
	private String name;

	public List<Integer> getJournalId() {
		return journalId;
	}

	public void setJournalId(List<Integer> journalId) {
		this.journalId = journalId;
	}

	public List<Integer> getExpertiseId() {
		return expertiseId;
	}

	public void setExpertiseId(List<Integer> expertiseId) {
		this.expertiseId = expertiseId;
	}

	public List<Integer> getSpecializationId() {
		return specializationId;
	}

	public void setSpecializationId(List<Integer> specializationId) {
		this.specializationId = specializationId;
	}

	public List<String> getInstitutionId() {
		return institutionId;
	}

	public void setInstitutionId(List<String> institutionId) {
		this.institutionId = institutionId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
