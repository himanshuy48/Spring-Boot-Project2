package com.digi.uniprr.VO;

public class MailTemplatesVO {

	private Integer emailTemplateId;
	 
	 private String email_template_name;
	 
	 private String from;
	 
	 private String to;
	 
	 private String cc;
	 
	 private String bcc;
	 
	 private String subject;
	 
	 private String body;

	public Integer getEmailTemplateId() {
		return emailTemplateId;
	}

	public void setEmailTemplateId(Integer emailTemplateId) {
		this.emailTemplateId = emailTemplateId;
	}

	public String getEmail_template_name() {
		return email_template_name;
	}

	public void setEmail_template_name(String email_template_name) {
		this.email_template_name = email_template_name;
	}

	public String getFrom() {
		return from;
	}

	public void setFrom(String from) {
		this.from = from;
	}

	public String getTo() {
		return to;
	}

	public void setTo(String to) {
		this.to = to;
	}

	public String getCc() {
		return cc;
	}

	public void setCc(String cc) {
		this.cc = cc;
	}

	public String getBcc() {
		return bcc;
	}

	public void setBcc(String bcc) {
		this.bcc = bcc;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public String getBody() {
		return body;
	}

	public void setBody(String body) {
		this.body = body;
	}
	 
}
